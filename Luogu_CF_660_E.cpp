using namespace std;
const int maxn=1e6+10,mod=1e9+7;
int n,m;
int ans;
namespace FastMod{
    inline void madd(int &x,int y){x+=y;(x>=mod)&&(x-=mod);}
    inline void mdel(int &x,int y){x-=y;(x<0)&&(x+=mod);}
    inline void mmul(int &x,int y){x=1ll*x*y%mod;}
    inline int imadd(int x,int y){madd(x,y);return x;}
    inline int imdel(int x,int y){mdel(x,y);return x;}
    inline int immul(int x,int y){mmul(x,y);return x;}
    inline int qpow(int x,int y){int res=1;while(y){if(y&1) res=1ll*res*x%mod;x=1ll*x*x%mod;y>>=1;}return res;}
}
using namespace FastMod;
int main(){
    scanf("%d%d",&n,&m);
    ans=qpow(m,n);
    for(int i=1;i<=n;i++)   madd(ans,immul(qpow(m,n+1-i),qpow(m+m-1,i-1)));
    printf("%d\n",ans);
}